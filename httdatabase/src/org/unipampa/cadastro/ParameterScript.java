package org.unipampa.cadastro;

public class ParameterScript {
	private int idscript;
	private String nmscript;
	private String dirlocation;
	private String scriptcommand;
	private String resultfilename;
	
	public int getIdscript() {
		return idscript;
	}
	public void setIdscript(int idscript) {
		this.idscript = idscript;
	}
	public String getNmscript() {
		return nmscript;
	}
	public void setNmscript(String nmscript) {
		this.nmscript = nmscript;
	}
	public String getDirlocation() {
		return dirlocation;
	}
	public void setDirlocation(String dirlocation) {
		this.dirlocation = dirlocation;
	}
	public String getScriptcommand() {
		return scriptcommand;
	}
	public void setScriptcommand(String scriptcommand) {
		this.scriptcommand = scriptcommand;
	}
	public String getResultfilename() {
		return resultfilename;
	}
	public void setResultfilename(String resultfilename) {
		this.resultfilename = resultfilename;
	}
	
	
}
