package org.unipampa.cadastro.htt;

public class Family {

	private int idfamily;
	private String nmfamily;
	private int idclasse;
	private int idsuperfamily;
	private String nmsuperfamily;
	
	public int getIdfamily() {
		return idfamily;
	}
	public void setIdfamily(int idfamily) {
		this.idfamily = idfamily;
	}
	public String getNmfamily() {
		return nmfamily;
	}
	public void setNmfamily(String nmfamily) {
		this.nmfamily = nmfamily;
	}
	public int getIdclasse() {
		return idclasse;
	}
	public void setIdclasse(int idclasse) {
		this.idclasse = idclasse;
	}
	public int getIdsuperfamily() {
		return idsuperfamily;
	}
	public void setIdsuperfamily(int idsuperfamily) {
		this.idsuperfamily = idsuperfamily;
	}
	public String getNmsuperfamily() {
		return nmsuperfamily;
	}
	public void setNmsuperfamily(String nmsuperfamily) {
		this.nmsuperfamily = nmsuperfamily;
	}
	
	
}
